package Slab.astah.aiso;


class AttributeModel{
	private String name;
	private String value;

	public void setName(String _name) {
		name = _name;
	}

	public String getName() {
		return name;
	}

	public void setValue(String _value){
		value = _value;
	}

	public String getValue() {
		return value;
	}
}